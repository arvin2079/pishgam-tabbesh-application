package com.demo.pishgamt3.Method_channels_Strings;

public class Header {
    //header have some final string
  private final  String kayheader="nameOfheader";
  private final   String keyvalue="valueOfheader";
  private final   String valueheader="Accept";
  private final   String valueval="application/json";

    public Header() {
    }


    public String getKayheader() {
        return kayheader;
    }

    public String getKeyvalue() {
        return keyvalue;
    }

    public String getValueheader() {
        return valueheader;
    }

    public String getValueval() {
        return valueval;
    }
}
